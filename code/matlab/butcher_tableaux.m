function [A, b, c] = butcher_tableaux(ID)
% A collection of Butcher tableaux

A = [];
b = [];
c = [];

%%% ------------------------------------ %%%
%%% ------ L-stable SDIRK methods ------ %%%
%%% ------------------------------------ %%%
if strcmp(ID, 'LSDIRK1')
    A = 1;
    b = 1;
    c = 1;

% 2nd-order [Dobrev et al. (2017)]
elseif strcmp(ID, 'LSDIRK2')
    gamma   = 1.0 - 1.0 / sqrt(2.0);
    A       = [gamma, 0.0; ...
               2*(1-gamma)-1, gamma];
    b       = [1/2; 1/2];
    c       = [gamma; 1-gamma];

% 3rd-order. Dobrev et al. (2017). Also see Butcher (2008), p. 262    
elseif strcmp(ID, 'LSDIRK3')
    x = 0.43586652150845899942; % Solved for root of cubic with high precision
    p = 0.5*(1+x);
    q = 0.5*(1-x);
    y = -3/2*x^2 + 4*x - 1/4;
    z =  3/2*x^2 - 5*x + 5/4;
    A = [x,  0,  0;
         q,  x,  0;
         y,  z,  x];
    c = [x;  p;  1];
    b = [y;  z;  x];

% 4th-order. L-stable
% HairerWanner1996, Table IV.6.5
% also in DuarteDobbinsSmooke2016, Appendix C    
elseif strcmp(ID, 'LSDIRK4')
    A   = [0.25, 0.0, 0.0, 0.0, 0.0; ...
           0.5, 0.25, 0.0, 0.0, 0.0; ...
           17.0/50.0, -1.0/25.0, 0.25, 0.0, 0.0; ...
           371.0/1360.0, -137.0/2720.0, 15.0/544.0, 0.25, 0.0; ...
           25.0/24.0, -49.0/48.0, 125.0/16.0, -85.0/12.0, 0.25];
    b   = A(end, :);
    c   = [0.25; ...
           0.75; ...
           11.0/20.0; ...
           0.5; ...
           1.0];
       
%%% ------------------------------------ %%%    
%%% ------ A-stable SDIRK methods ------ %%%   
%%% ------------------------------------ %%%
% % 4th-order, 3-stage SDIRK. A-stable. NOT L-stable
% % See MFEM's ode.hpp/ode.cpp
elseif strcmp(ID, 'ASDIRK4')
    alpha = 1/sqrt(3)*cos(pi/18) + 0.5;
    beta = 1/(6*(2*alpha-1)*(2*alpha-1));
    A = [alpha, 0.0, 0.0; ...
         0.5-alpha, alpha, 0.0; ...
         2*alpha, 1-4*alpha, alpha];
    b = [beta; ...
         1-2*beta; ... 
         beta];
    c = [alpha; ... 
         0.5; ...
         1-alpha];

       
%%% --------------------------- %%%    
%%% ------ Radau methods ------ %%%   
%%% --------------------------- %%%
elseif strcmp(ID, 'RadauIIA3')
%     A = [0.4166666666666666,-0.0833333333333333; 
%          0.75,0.25];
%     b = [0.75,0.25];
%     c = [0.3333333333333333,1.];    
    A = [5/12, -1/12;
         3/4, 1/4];
    b = [3/4, 1/4];
    c = [1/3, 1];
    
elseif strcmp(ID, 'RadauIIA5')    
%     A = [0.1968154772236604,-0.06553542585019835,0.02377097434822015; 
%          0.3944243147390872,0.2920734116652285,-0.04154875212599793; 
%          0.3764030627004673,0.5124858261884215,0.1111111111111111];
%     b = [0.3764030627004673,0.5124858261884215,0.1111111111111111];
%     c = [0.1550510257216822,0.6449489742783178,1.];
    A = [11/45-7*sqrt(6)/360, 37/225-169*sqrt(6)/1800, -2/225+sqrt(6)/75;
         37/225+169*sqrt(6)/1800, 11/45+7*sqrt(6)/360, -2/225-sqrt(6)/75;
         4/9-sqrt(6)/36, 4/9+sqrt(6)/36, 1/9];
    b = [4/9-sqrt(6)/36, 4/9+sqrt(6)/36, 1/9];
    c = [2/5-sqrt(6)/10, 2/5+sqrt(6)/10, 1];
    
    
elseif strcmp(ID, 'RadauIIA7')
    A = [0.112999479323156186,-0.0403092207235222057,0.0258023774203363910,-0.00990467650726642390;
         0.234383995747400257,0.206892573935358900,-0.0478571280485407189,0.0160474228065162730;
         0.216681784623250342,0.406123263867373311,0.189036518170056342,-0.0241821048998329395;
         0.220462211176768375,0.388193468843171881,0.328844319980059744,0.0625000000000000000];
    b = [0.220462211176768375,0.388193468843171881,0.328844319980059744,0.0625000000000000000];
    c = [0.0885879595127039474,0.409466864440734711,0.787659461760847056,1.00000000000000000];
    
elseif strcmp(ID, 'RadauIIA9')        
    A = [0.0729988643179033243,-0.0267353311079455719,0.0186769297639843544,-0.0128791060933064399,0.00504283923388201521;
         0.153775231479182469,0.146214867847493507,-0.0364445689051280895,0.0212330631193047194,-0.00793557990272877753;
         0.140063045684809872,0.298967129491283479,0.167585070135248963,-0.0339691016866177466,0.0109442887441922523;
         0.144894308109534758,0.276500068760159228,0.325797922910421030,0.128756753254909761,-0.0157089173788053284;
         0.143713560791225941,0.281356015149462060,0.311826522975741254,0.223103901083570744,0.0400000000000000000];
    b = [0.143713560791225941,0.281356015149462060,0.311826522975741254,0.223103901083570744,0.0400000000000000000];
    c = [0.0571041961145176822,0.276843013638123828,0.583590432368916820,0.860240135656219448,1.00000000000000000];

    
%%% ----------------------------- %%%    
%%% ------ Lobatto methods ------ %%%   
%%% ----------------------------- %%%
% All LobattoIIC methods are L-stable, and are of non-stiff order 2s-2
%
% For all these tableaux, see "Lobatto Methods," Laurent O. Jay, 
% Department of Mathematics, The University of Iowa,
% Iowa City, IA, USA (Table 3)

elseif strcmp(ID, 'LobIIIC2')    
    A = [0.5,-0.5; 
         0.5,0.5];
    b = [0.5,0.5];
    c = [0.,1.];
    
% 3-stage, 4th-order    
elseif strcmp(ID, 'LobIIIC4')        
%     A = [0.1666666666666667,-0.3333333333333333,0.1666666666666667; 
%          0.1666666666666667,0.4166666666666666,-0.0833333333333333; 
%          0.1666666666666667,0.6666666666666666,0.1666666666666667];
%     b = [0.1666666666666667,0.6666666666666666,0.1666666666666667];
%     c = [0.,0.5,1.];
    A = [1/6, -1/3, 1/6;
         1/6, 5/12, -1/12;
         1/6, 2/3,  1/6];
    b = [1/6, 2/3, 1/6];
    c = [0, 1/2, 1];

% 4-stage, 6th-order. 
elseif strcmp(ID, 'LobIIIC6')        
%     A = [0.0833333333333333,-0.1863389981249825,0.1863389981249825,-0.0833333333333333; 
%          0.0833333333333333,0.25,-0.0942079307083088,0.03726779962499649; 
%          0.0833333333333333,0.4275412640416421,0.25,-0.03726779962499649; 
%          0.0833333333333333,0.4166666666666666,0.4166666666666666,0.0833333333333333];
%     b = [0.0833333333333333,0.4166666666666666,0.4166666666666666,0.0833333333333333];
%     c = [0.,0.276393202250021,0.723606797749979,1.];
    A = [1/12, -sqrt(5)/12,       sqrt(5)/12,        -1/12;
         1/12, 1/4,               (10-7*sqrt(5))/60, sqrt(5)/60;
         1/12, (10+7*sqrt(5))/60, 1/4,               -sqrt(5)/60;
         1/12, 5/12,              5/12, 1/12];
    b = [1/12, 5/12, 5/12, 1/12];
    c = [0, 1/2-sqrt(5)/10, 1/2+sqrt(5)/10, 1];


% 5-stage, 8th-order. 
elseif strcmp(ID, 'LobIIIC8')     
    A = [1/20, -7/60,                   2/15,                 -7/60,                   1/20;
         1/20, 29/180,                  (47-15*sqrt(21))/315, (203-30*sqrt(21))/1260,  -3/140;
         1/20, (329+105*sqrt(21))/2880, 73/360,               (329-105*sqrt(21))/2880, 3/160;
         1/20, (203+30*sqrt(21))/1260,  (47+15*sqrt(21))/315, 29/180,                  -3/140;
         1/20, 49/180,                  16/45,                49/180,                  1/20];
     b = [1/20, 49/180, 16/45, 49/180, 1/20];
     c = [0, 1/2-sqrt(21)/14, 1/2, 1/2+sqrt(21)/14, 1];

%%% --------------------------- %%%    
%%% ------ Gauss methods ------ %%%   
%%% --------------------------- %%%
% 2nd-order implicit midpoint rule
elseif strcmp(ID, 'Gauss2') 
    A = 1/2;
    b = 1;
    c = 1/2;

% % implicit 4th-order method, Hammer & Hollingsworth (A-stable)
% % note: coincides with s=2-stage, p=2s-order Gauss method
% % %       see https://www.math.auckland.ac.nz/~butcher/ODE-book-2008/Tutorials/IRK.pdf    
elseif strcmp(ID, 'Gauss4')           
    A = [0.25,               0.25-sqrt(3.0)/6.0; 
         0.25+sqrt(3.0)/6.0, 0.25];
    b = [0.5, 0.5];
    c = [0.5-sqrt(3.0)/6.0; 0.5+sqrt(3.0)/6.0];
    
elseif strcmp(ID, 'Gauss6')           
    A = [5/36,             2/9-sqrt(15)/15, 5/36-sqrt(15)/30; 
         5/36+sqrt(15)/24, 2/9,             5/36-sqrt(15)/24; 
         5/36+sqrt(15)/30, 2/9+sqrt(15)/15, 5/36];
    b = [5/18; 4/9; 5/18];
    c = [1/2-sqrt(15)/10; 1/2; 1/2+sqrt(15)/10];
    
elseif strcmp(ID, 'Gauss8')           
    A = [0.0869637112843634643,-0.0266041800849987933,0.0126274626894047245,-0.00355514968579568316;
         0.188118117499868072,0.163036288715636536,-0.0278804286024708952,0.00673550059453815552;
         0.167191921974188773,0.353953006033743967,0.163036288715636536,-0.0141906949311411430;
         0.177482572254522612,0.313445114741868347,0.352676757516271865,0.0869637112843634643];
    b = [0.173927422568726929,0.326072577431273071,0.326072577431273071,0.173927422568726929];
    c = [0.0694318442029737124,0.330009478207571868,0.669990521792428132,0.930568155797026288];
    
elseif strcmp(ID, 'Gauss10')           
    A = [0.0592317212640472719,-0.0195703643590760375,0.0112544008186429556,-0.00559379366081218488,0.00158811296786599854;
         0.128151005670045283,0.119657167624841617,-0.0245921146196422004,0.0103182806706833574,-0.00276899439876960304;
         0.113776288004224603,0.260004651680641519,0.142222222222222222,-0.0206903164309582846,0.00468715452386994123;
         0.121232436926864147,0.228996054578999877,0.309036559064086645,0.119657167624841617,-0.00968756314195073974;
         0.116875329560228545,0.244908128910495419,0.273190043625801489,0.258884699608759272,0.0592317212640472719];
    b = [0.118463442528094544,0.239314335249683234,0.284444444444444444,0.239314335249683234,0.118463442528094544];
    c = [0.0469100770306680036,0.230765344947158454,0.500000000000000000,0.769234655052841546,0.953089922969331996];
    
else
    error('Butcher tableau ''%s'' not recognised...', ID)

end

if isempty(A) || isempty(b) || isempty(c)
    error('Ooops... I recognise Butcher tableau ''%s'', but have not implemented it!')
end

% Ensure column vectors
b = b(:);
c = c(:);

end